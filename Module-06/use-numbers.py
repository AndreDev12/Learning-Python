# Conversión de cadenas en números
demo_int = int('215')
print(demo_int)

demo_float = float('215.3')
print(demo_float)

# Valores absolutos
print(abs(39 - 16))
print(abs(16 - 39))

# Redondeo
print(round(14.5))

# Biblioteca matemática
from math import ceil, floor

round_up = ceil(12.5)
print(round_up)

round_down = floor(12.5)
print(round_down)